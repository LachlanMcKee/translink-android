package com.lach.translink.ui.impl.search.dialog;

class WebSiteInfo {

    private final String uri;
    private final String name;

    public WebSiteInfo(String uri, String name) {
        super();
        this.uri = uri;
        this.name = name;
    }

    public String getUri() {
        return uri;
    }

    public String getName() {
        return name;
    }

}
